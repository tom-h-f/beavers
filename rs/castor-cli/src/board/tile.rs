impl Model {
    pub fn load(&self, asset_server: &Res<AssetServer>) -> Handle<Scene> {
        asset_server.load(GltfAssetLabel::Scene(0).from_asset(format!("models/{:?}.glb", self)))
    }
}

impl Tile {
    pub fn load(&self, asset_server: &Res<AssetServer>) -> Handle<Scene> {
        asset_server.load(GltfAssetLabel::Scene(0).from_asset(format!("models/{:?}.glb", self)))
    }
}
